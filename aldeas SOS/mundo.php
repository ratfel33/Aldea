                <h2>Aldeas SOS en el mundo</h2>
                </br>
                
                <p align="justify">En abril de 1949, Hermann Gmeiner fundó, con varios amigos, la “Societas Socialis”; una sociedad de jóvenes que se propuso esforzarse por desarrollar una efectiva labor de protección a menores de edad, utilizando las siglas de SOS; que de acuerdo a la señal internacional de socorro marítimo, sugería AYUDA; en este caso, para los niños en situación de desamparo.
                </br>
                 </br>

La primera Aldea Infantil SOS se construyó en Imst, pequeña ciudad del Tirol en Austria en 1949, como una respuesta a la gran necesidad de atención a los niños y niñas que habían quedado en orfandad por la Segunda Guerra Mundial.
</br>
 </br>
En 1964 se crea la Federación SOS-KINDERDORF INTERNATIONAL, para apoyar fundaciones en países de Europa y Estados Unidos y así trabajar directamente con Proyectos SOS en los países en vías de desarrollo.
