                <h2>Aldeas SOS en Nicaragua</h2>
                </br>
                
                <p align="justify">
En Nicaragua la primera Aldea Infantil SOS se construye en Estelí en 1973, frente a la necesidad de atención a miles de niñas y niños en orfandad, luego del devastador terremoto de 1972.
    </br>
    </br>
La construcción inicial consistía en casas prefabricadas de madera. La Aldea Infantil SOS Estelí inició con 4 madres y 16 niños y niñas. En la actualidad cuenta con 14 familias SOS.
    </br>
    </br>
En 1978 estalló la guerra civil en Nicaragua, muriendo muchas personas. Las altas tasas de desempleo, con la consecuente pobreza económica y social de las familias, llevaron a la construcción de más Aldeas Infantiles SOS y otras instituciones de atención en el oeste y centro de Nicaragua. En enero de 1980 la Junta de Gobierno Municipal de Juigalpa, dona los terrenos para la construcción de la Aldea Infantil SOS Juigalpa. La Aldea Infantil SOS inicia operaciones el 5 de abril de 1981 con un total de 8 hogares familiares.
    </br>
    </br>
En 1993 el Concejo Municipal de la Alcaldía de Managua resuelve conceder en carácter de donación el terreno donde se construyó la Aldea Infantil SOS, el Centro Social SOS Hermann Gmeiner y el Colegio SOS Hermann Gmeiner de Managua. El 27 de noviembre de 1995 se abren las primeras cuatro casas familiares de la Aldea Infantil SOS Managua con 35 niños y niñas.
    </br>
    </br>
Luego de que Huracán Mitch azotara fuertemente la parte occidental del país, la idea de crear una Aldea en León tomo fuerza. Aldeas Infantiles SOS Nicaragua construye la Aldea Infantil SOS y el Centro Social SOS Hermann Gmeiner León, el 20 de agosto del 2004.
    </br>
    </br>
A partir del año 2005 se extiende la experiencia del programa de Fortalecimiento Familiar a los 8 Centros de cuido y protección diaria (Jardín de Infantes, CDIs y Centros Sociales SOS Hermann Gmeiner).
    </br>
    </br>
La principal razón para la construcción de la Aldea Infantil SOS Rivas, son los elevados índices de explotación sexual comercial infantil como departamento, aunque nuestro modelo de atención no está dirigido hacia este tipo de problemática, si consideramos que esta situación generan niños y niñas en condiciones de desamparo. Se inicia la construcción en el mes de octubre 2006 y se concluye en el mes de agosto del 2007.
    </br>
    </br>
En el Municipio de Somoto Departamento de Madriz, Aldeas Infantiles SOS Nicaragua, crea el Programa de Fortalecimiento Familiar Somoto en marzo del año 2008, el que se focaliza en las familias de extrema pobreza, en las cuales se potencian capacidades y competencias para que se organicen en sus comunidades y, mediante la autogestión y apoyo de la organización, se logra instalar cuatro centros y un hogar comunitario, con el objetivo de brindar protección a niños y niñas, a fin de evitar o prevenir el abandono infantil.
En Nicaragua, actualmente atendemos a 6,000 niños, niñas y sus familias en ocho localidades:
    </br>
    </br>
Estelí
</br>

León
</br>

Rivas
</br>

Managua
</br>

Matagalpa
</br>

Juigalpa
</br>

Jinotega
</br>

Somoto

</br>
    </br>
Contamos con 6 Aldeas Infantiles SOS, 8 Centros Sociales y 36 Centros y Hogares Comunitarios ubicados en barrios urbanos de estas localidades.

</br>
    </br>
<h3>Fortalecimiento Familiar</h3>


</br>
    
Aldeas Infantiles SOS Nicaragua reconoce como principio fundamental que las familias son los espacios privilegiados para el desarrollo integral de sus hijos e hijas, por tanto, a través del Programa de Fortalecimiento Familiar y Desarrollo Comunitario, contribuye a la disminución del abandono infantil, evitando que niñas y niños sean privados de los cuidados de sus familiares, enfrentando las causas. Nuestro enfoque es trabajar con las familias y sus comunidades apoyándolas a mejorar sus capacidades de proteger y cuidar a sus hijos e hijas, de manera que estos puedan crecer en un entorno familiar protegido.

</br>
    </br>
¿Cómo logramos esto? Facilitando procesos de formación y capacitación a las familias participantes y a las organizaciones comunitarias de la localidad contribuyendo con su empoderamiento y desarrollo de capacidades como auto cuido, protección, gestión de recursos y auto sostenibilidad; todo en función de crear entornos familiares más protectores a sus hijos e hijas. 

</br>
    </br>
Actualmente contamos con diez Programas que brindan atención a 2,700 niños y niñas de Estelí, Juigalpa, Managua, Matagalpa, León, Somoto y Jinotega. El Programa se desarrolla en dos modalidades de atención: Ocho Centros sociales, espacios institucionales de cuido diario, alimentación y educación en los que se atienden alrededor de 175 niños y niñas entre 3 meses y 12 años de edad, pertenecientes a familias en situación de vulnerabilidad y riesgo social. Estos Centros Sociales se insertan en las comunidades aledañas a través de treinta y seis Hogares Comunitarios y Centros Comunitarios, espacios comunitarios dentro de barrios de media y alta vulnerabilidad social donde se atienden a 25 o 45 niños y niñas (respectivamente) entre los 2 y 6 años de edad, con cuido diario, alimentación y educación. En ambas modalidades el niño y la niña son el centro de atención que cubrimos a través del trabajo con madres, padres y otros miembros de la familia logrando en ellos capacidades para crear o mantener entornos familiares saludables y contenedores.

</br>
    </br>
