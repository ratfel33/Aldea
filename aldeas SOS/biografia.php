                <h2>Biografia de Hermann Gmeiner</h2>
                </br>

                        <img class="img-responsive" src="./img/herman.gif" height="180">

                <p align="justify"> Hermann Gmeiner nació en Austria en 1919, un año después de que la “Gran Guerra”, la Primera Guerra Mundial, llegara a su fin.  Fue el sexto entre los nueve hijos que tuvieron sus padres, y luego de la muerte de su madre cuando él contaba con tan sólo 5 años, su hermana mayor Elsa tomó el rol de madre en cuanto al cuidado de sus hermanos.
                </br>
                </br>
Debido a su rendimiento en la escuela del pueblo, obtuvo una beca que le permitió continuar sus estudios. Eran tiempos en los que Europa se encontraba una vez más bajo el horror de la guerra, y Austria no era ajena a ello, por lo que Hermann fue reclutado como muchos otros jóvenes y fue enviado en 1940 al frente oriental donde se enfrentaban la Alemania nazi y la Unión Soviética.
</br>
</br>
Fue una etapa determinante en su vida, en la que pudo ser testigo de lo que una guerra puede llegar a significar, siendo una de las cosas que más le afectaron el hecho de que millones de niños tuvieran que crecer sin amparo alguno, sin familia y sin hogar.
</br>
</br>
En 1945 regresó a casa y habiendo sido varias veces herido, permaneció en el hospital cerca de un año. Trabajando como ayudante en la Iglesia de su pueblo durante 1947, al encontrarse con un niño huérfano de 12 años su mente lo llevaría a todas esas vivencias y decidiría por fin a qué dedicar su vida.
</br>
</br>
Comenzó a visitar institutos educativos, casas de cuidado, y dándose cuenta de que no había nada que estuviera diseñado según lo que para él eran las necesidades de niños en esa situación, fue construyendo una idea que pudiera llenar ése vacío. Así, con todo el dinero que poseía, el equivalente  a unos 40 dólares americanos, fundó en 1949 la “Asociación de Aldeas Infantiles SOS” y el mismo año la primera Aldea Infantil SOS en Austria.
</br>
</br>
Hoy existen unas 438 Aldeas Infantiles en 132 países entre los que se encuentran Venezuela, Alemania, España, y varios países africanos, siendo unos 500.000 los niños y jóvenes beneficiados por sus labores.
Las “Aldeas Infantiles SOS” se basan en cuatro principios: la Madre, los Hermanos, la Casa y la Aldea. Todos enlazados ellos constituyen el escenario en el que los niños y jóvenes reciben lo necesario para poder cubrir aspectos socio-afectivos que por diversas razones pudiera haberles faltado. Todo un esfuerzo inmenso por brindar un hogar a quienes no pudieron tenerlo de forma natural.
</br>
</br>
“Desde mi punto de vista, nada en el mundo es más importante que proteger a un niño” dijo en algún momento de su vida Hermann Gmeiner, quien murió en 1986 en su Austria natal y fue enterrado en la Aldea Infantil de Imst, la primera que construyó.
</br>
</br>
La información aquí suministrada ha sido redactada por el equipo del Proyecto Nueva Inspiración, con los datos recogidos de diversos medios de comunicación. En la mayoría de los casos que reseñamos, se están gestionando entrevistas e incluso visitas a los sitios en los que se desarrolla la historia, para poder así tomar de primera mano los datos que más nos interesen.

